void
LCD_PRINT_FONT1(unsigned char data,unsigned char selection)
{
  unsigned char i,index;
  index	= data - 32;
  LCD_PAGE_COL_CHECK_FONT1();
  
  for(i = 0; i < 6; i++)
  {
    if(selection == 1)
    {
      LCD_WRITE_DATA(~ALPHA[index][i]);
    }
    else
    {
      LCD_WRITE_DATA(ALPHA[index][i]);
    }
    COL += 1;
  }
}

void
LCD_PAGE_COL_CHECK_FONT1(void)
{
  if (COL >= 127)
  {
    PAGE += 1;
    if(PAGE >= 0xB7 + 1)
      lcd_clear();
    COL = 1;
    lcd_set_cursor(PAGE,COL);
  }
}

