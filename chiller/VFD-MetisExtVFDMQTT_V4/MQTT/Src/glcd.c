void lcd_tx_byte(uint8_t u8Data)
{
  GPIOC->BRR = PIN_D3;
  GPIOD->BRR = 0x805f;
  if(u8Data & (1<<0))
  {
    GPIOD->BSRR = PIN_D0;
  }
  if(u8Data & (1<<1))
  {
    GPIOD->BSRR = PIN_D1;
  }
  if(u8Data & (1<<2))
  {
    GPIOD->BSRR = PIN_D2;
  }
  if(u8Data & (1<<3))
  {
    GPIOC->BSRR = PIN_D3;
  }
  if(u8Data & (1<<4))
  {
    GPIOD->BSRR = PIN_D4;
  }
  if(u8Data & (1<<5))
  {
    GPIOD->BSRR = PIN_D5;
  }
  if(u8Data & (1<<6))
  {
    GPIOD->BSRR = PIN_D6;
  }
  if(u8Data & (1<<7))
  {
    GPIOD->BSRR = PIN_D7;
  }
}

void
LCD_WRITE_CMD(unsigned char cmd)
{
  GPIOD->BRR = GPIO_PIN_13;
  GPIOD->BRR = GPIO_PIN_7;
  lcd_tx_byte(cmd);
  GPIOD->BSRR = GPIO_PIN_7;
  GPIOD->BSRR = GPIO_PIN_13;
}

void
lcd_set_contrast(uint8_t val)
{
  LCD_WRITE_CMD(0x81);
  
  if (val > 63) 
  {
    LCD_WRITE_CMD(63);
  }
  else
  {
    LCD_WRITE_CMD(val);
  }
}

void
LCD_WRITE_DATA(uint8_t data)
{
  GPIOD->BSRR = GPIO_PIN_13;
  GPIOD->BRR = GPIO_PIN_7;
  lcd_tx_byte(data);
  GPIOD->BSRR = GPIO_PIN_7;
  GPIOD->BRR = GPIO_PIN_13;
}

void
lcd_set_cursor(uint8_t line, uint8_t col)
{
  unsigned char msb,lsb;
  msb = (((col & 0xF0) >> 4)| 0x10);            //ColAdd(7:4)
  lsb = (col & 0x0F);                           //ColAdd(3:0)
  LCD_WRITE_CMD(line);
  LCD_WRITE_CMD(msb);
  LCD_WRITE_CMD(lsb);
}


void
lcd_clear(void)
{
  uint8_t row;
  for(row = 0xB0;row < 0xB8;row++)
  {
    lcd_set_cursor(row,0);
    for(uint8_t col = 0;col <128;col++)
    {
      LCD_WRITE_DATA(0x00);
    }
  }
}


